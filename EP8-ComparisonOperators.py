#(8)ตัวดำเนินการเปรียบเทียบ

# นำข้อมูลอย่างน้อย 2 ตัว
# ชนิดเดียวกัน
# คำตอบ จริง หรือ เท็จ

x,y = 10, 5 

print("x มากกว่า y หรือไม่ ? : ",x>y)
print("x น้อยกว่า y หรือไม่ ? : ",x<y)
print("x เท่ากับ y หรือไม่ ? : ",x==y)
print("x ไม่เท่ากับ y หรือไม่ ? : ",x!=y)
print("-------------------------------")

print("x มากกว่า หรือ เท่ากับ y หรือไม่ ? : ",x>=y) #ต้องเป็นจริง 1 เงื่อนไขจะได้เป็น True
print("x น้อยกว่า หรือ เท่ากับ y หรือไม่ ? : ",x<=y)
